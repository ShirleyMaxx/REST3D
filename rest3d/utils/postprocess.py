def make_wall_obj(v0, v1, normal_axis: int, pos_along_normal: float,
                  y_range, out_path: str, outward_sign: int = 1):
    thickness = 0.01 * outward_sign
    y0, y1 = y_range
    corners = [
        [v0, y0, pos_along_normal],
        [v1, y0, pos_along_normal],
        [v1, y1, pos_along_normal],
        [v0, y1, pos_along_normal],
    ]
    back_corners = [[c[0], c[1], c[2] + thickness] for c in corners]

    def reorder(c):
        if normal_axis == 0:
            return [c[2], c[1], c[0]]
        return c

    all_verts = [reorder(c) for c in corners] + [reorder(c) for c in back_corners]
    if outward_sign >= 0:
        faces = [[1,2,3,4],[8,7,6,5],[1,2,6,5],[4,3,7,8],[1,5,8,4],[2,6,7,3]]
    else:
        faces = [[4,3,2,1],[5,6,7,8],[5,6,2,1],[8,7,3,4],[4,8,5,1],[3,7,6,2]]
    os.makedirs(os.path.dirname(out_path) or ".", exist_ok=True)
    with open(out_path, "w") as f:
        f.write("# Wall mesh generated by postprocess.py\n")
        for v in all_verts:
            f.write(f"v {v[0]:.4f} {v[1]:.4f} {v[2]:.4f}\n")
        for fc in faces:
            f.write("f " + " ".join(str(i) for i in fc) + "\n")


def make_wall_obj_oriented(p0_xz, p1_xz, outward_n_xz, y_range, out_path: str):
    thickness = 0.01
    y0, y1 = y_range
    p0 = np.array(p0_xz, dtype=np.float64)
    p1 = np.array(p1_xz, dtype=np.float64)
    n  = np.array(outward_n_xz, dtype=np.float64)
    n  = n / max(1e-12, np.linalg.norm(n))
    t  = thickness * n
    front = [[p0[0],y0,p0[1]],[p1[0],y0,p1[1]],[p1[0],y1,p1[1]],[p0[0],y1,p0[1]]]
    back  = [[p0[0]+t[0],y0,p0[1]+t[1]],[p1[0]+t[0],y0,p1[1]+t[1]],
             [p1[0]+t[0],y1,p1[1]+t[1]],[p0[0]+t[0],y1,p0[1]+t[1]]]
    all_verts = front + back
    faces = [[1,2,3,4],[8,7,6,5],[1,2,6,5],[4,3,7,8],[1,5,8,4],[2,6,7,3]]
    os.makedirs(os.path.dirname(out_path) or ".", exist_ok=True)
    with open(out_path, "w") as f:
        f.write("# Oriented wall mesh generated by postprocess.py\n")
        for v in all_verts:
            f.write(f"v {v[0]:.4f} {v[1]:.4f} {v[2]:.4f}\n")
        for fc in faces:
            f.write("f " + " ".join(str(i) for i in fc) + "\n")


def export_transformed_obj(src_path: str, pos, rot_xyzw, out_path: str):
    R = quat_to_rotmat(rot_xyzw)
    lines_out = []
    with open(src_path) as f:
        for line in f:
            if line.startswith("v "):
                p = line.split()
                v = np.array([float(p[1]), float(p[2]), float(p[3])])
                vw = R @ v + np.array(pos)
                extra = (" " + " ".join(p[4:])) if len(p) > 4 else ""
                lines_out.append(f"v {vw[0]:.6f} {vw[1]:.6f} {vw[2]:.6f}{extra}\n")
            else:
                lines_out.append(line)
    os.makedirs(os.path.dirname(out_path) or ".", exist_ok=True)
    with open(out_path, "w") as f:
        f.writelines(lines_out)


