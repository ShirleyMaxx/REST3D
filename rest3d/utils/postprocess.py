def _convex_hull_xz_points(xy: np.ndarray) -> np.ndarray:
    if xy.shape[0] < 3:
        return xy
    rnd  = np.round(xy.astype(np.float64), decimals=5)
    xy_u = np.unique(rnd, axis=0)
    if xy_u.shape[0] < 3:
        return xy_u
    order = np.lexsort((xy_u[:, 1], xy_u[:, 0]))
    pts   = xy_u[order]

    def cross(o, a, b):
        return (a[0]-o[0])*(b[1]-o[1]) - (a[1]-o[1])*(b[0]-o[0])

    lower = []
    for p in pts:
        while len(lower) >= 2 and cross(lower[-2], lower[-1], p) <= 0:
            lower.pop()
        lower.append(tuple(p))
    upper = []
    for p in reversed(pts):
        while len(upper) >= 2 and cross(upper[-2], upper[-1], p) <= 0:
            upper.pop()
        upper.append(tuple(p))
    return np.array(lower[:-1] + upper[:-1], dtype=np.float64)


def _wall_depth_ref_xz(xz: np.ndarray, plane_p: np.ndarray, plane_inward: np.ndarray,
                       policy: str) -> float:
    if policy == "min":
        return float(((xz - plane_p[None, :]) @ plane_inward).min())
    if policy == "hull_aabb":
        n    = xz.shape[0]
        xz_s = xz[:: max(1, n // 5000)] if n > 5000 else xz
        hull  = _convex_hull_xz_points(xz_s.astype(np.float64))
        d_hull = (hull - plane_p) @ plane_inward
        lo = xz.min(axis=0); hi = xz.max(axis=0)
        corners = np.array([[lo[0],lo[1]],[lo[0],hi[1]],[hi[0],lo[1]],[hi[0],hi[1]]], dtype=np.float64)
        d_box   = (corners - plane_p) @ plane_inward
        return float(min(d_hull.min(), d_box.min()))
    if policy == "max":
        return float(((xz - plane_p[None, :]) @ plane_inward).max())
    raise ValueError(f"unknown wall depth policy {policy!r}")


def determine_primary_wall(floor_wall_names, wall_names, world_aabbs, scene_min, scene_max):
    valid_fw   = [n for n in floor_wall_names if n in world_aabbs]
    valid_wall = [n for n in wall_names       if n in world_aabbs]

    def _footprint(name):
        lo, hi = world_aabbs[name]
        return max(1e-6, float((hi[0]-lo[0])*(hi[2]-lo[2])))

    def _face_gaps(name):
        lo, hi = world_aabbs[name]
        return {
            "X_min": float(lo[0]-scene_min[0]),
            "X_max": float(scene_max[0]-hi[0]),
            "Z_min": float(lo[2]-scene_min[2]),
            "Z_max": float(scene_max[2]-hi[2]),
        }

    def _label_to_axis_side(label):
        return {"X_min":(0,False),"X_max":(0,True),"Z_min":(2,False),"Z_max":(2,True)}[label]

    if valid_fw:
        fw_votes  = {"X_min":0.0,"X_max":0.0,"Z_min":0.0,"Z_max":0.0}
        fw_groups = {k:[] for k in fw_votes}
        for n in valid_fw:
            gaps = _face_gaps(n)
            best = min(gaps, key=gaps.get)
            w    = _footprint(n)
            fw_votes[best]  += w
            fw_groups[best].append((n, gaps[best], w))
            print(f"  [WallDetect/fw] {n}: X_min={gaps['X_min']:.3f} X_max={gaps['X_max']:.3f} "
                  f"Z_min={gaps['Z_min']:.3f} Z_max={gaps['Z_max']:.3f} -> {best} (w={w:.3f})")

        aux_votes = {"X_min":0.0,"X_max":0.0,"Z_min":0.0,"Z_max":0.0}
        for n in valid_wall:
            gaps = _face_gaps(n)
            best = min(gaps, key=gaps.get)
            aux_votes[best] += 0.25 * _footprint(n)

        total_votes = {k: fw_votes[k]+aux_votes[k] for k in fw_votes}
        print("  [WallDetect/vote] floor-wall:", "  ".join(f"{k}={v:.3f}" for k,v in total_votes.items()))

        best_vote = max(total_votes.values())
        cand = [k for k,v in total_votes.items() if abs(v-best_vote) < 1e-8]
        if len(cand) == 1:
            chosen = cand[0]
        else:
            def _mean_gap(label):
                grp = fw_groups[label]
                if not grp: return 1e9
                ws = np.array([x[2] for x in grp]); gs = np.array([x[1] for x in grp])
                return float((ws*gs).sum() / max(1e-9, ws.sum()))
            chosen = min(cand, key=_mean_gap)
            print(f"  [WallDetect/vote] tie {cand} -> choose {chosen} by mean-gap")

        normal_axis, at_max_side = _label_to_axis_side(chosen)
        anchors = [n for (n,_,_) in fw_groups[chosen]] or valid_fw
        if at_max_side:
            back_face = max(world_aabbs[n][1][normal_axis] for n in anchors)
            back_wall_pos = back_face + WALL_MARGIN
        else:
            back_face = min(world_aabbs[n][0][normal_axis] for n in anchors)
            back_wall_pos = back_face - WALL_MARGIN
        print(f"  [WallDetect] chosen={chosen}  anchors={anchors}  "
              f"back_face={back_face:.4f}  back_wall_pos={back_wall_pos:.4f}")
        return normal_axis, at_max_side, back_wall_pos

    if not valid_wall:
        return 2, True, scene_max[2] + WALL_MARGIN

    candidates = [(0,True,scene_max[0]),(0,False,scene_min[0]),(2,True,scene_max[2]),(2,False,scene_min[2])]
    scores = {}
    for (axis, at_max, bound) in candidates:
        gaps = [abs((world_aabbs[n][1][axis] if at_max else world_aabbs[n][0][axis]) - bound) for n in valid_wall]
        scores[(axis, at_max)] = np.mean(gaps)
    best = min(scores, key=scores.get)
    normal_axis, at_max_side = best
    extents = [world_aabbs[n][1][normal_axis] if at_max_side else world_aabbs[n][0][normal_axis] for n in valid_wall]
    back_face = max(extents) if at_max_side else min(extents)
    back_wall_pos = back_face + WALL_MARGIN if at_max_side else back_face - WALL_MARGIN
    return normal_axis, at_max_side, back_wall_pos


def compute_perp_wall_positions(floor_wall_names, wall_names, world_aabbs,
                                perp_axis, scene_min, scene_max):
    SNAP_DIST = 0.30
    combined  = list({n for n in (floor_wall_names+wall_names) if n in world_aabbs})

    at_max = [n for n in combined if (scene_max[perp_axis]-world_aabbs[n][1][perp_axis]) <= SNAP_DIST]
    side_max_pos = (max(world_aabbs[n][1][perp_axis] for n in at_max) + WALL_MARGIN
                    if at_max else scene_max[perp_axis] + SIDE_WALL_MARGIN)

    at_min = [n for n in combined if (world_aabbs[n][0][perp_axis]-scene_min[perp_axis]) <= SNAP_DIST]
    side_min_pos = (min(world_aabbs[n][0][perp_axis] for n in at_min) - WALL_MARGIN
                    if at_min else scene_min[perp_axis] - SIDE_WALL_MARGIN)

    return side_min_pos, side_max_pos


def assign_wall_objects_to_walls(wall_names, world_aabbs, normal_axis, at_max_side,
                                  back_wall_pos, side_min_pos, side_max_pos,
                                  front_wall_pos=None) -> dict:
    SNAP_DIST               = 0.05
    CLUSTER_DIST            = 0.80
    SNAP_MAX_DEPTH_FOR_BACK = SNAP_DIST * 4
    SNAP_SIDE_CENTROID_RATIO = 8.0
    SNAP_SIDE_CENTROID_MIN   = 0.12

    perp_axis = 2 if normal_axis == 0 else 0
    valid     = [n for n in wall_names if n in world_aabbs]
    assignments: dict = {}

    def _gap_back(lo, hi):
        return abs(back_wall_pos - (hi[normal_axis] if at_max_side else lo[normal_axis]))
    def _gap_side_mn(lo, hi):
        return abs(lo[perp_axis] - side_min_pos)
    def _gap_side_mx(lo, hi):
        return abs(hi[perp_axis] - side_max_pos)
    def _gap_front(lo, hi):
        return abs(front_wall_pos - (lo[normal_axis] if at_max_side else hi[normal_axis]))

    remaining = []
    for name in valid:
        lo, hi = world_aabbs[name]
        gaps = {"back": _gap_back(lo,hi), "side_min": _gap_side_mn(lo,hi), "side_max": _gap_side_mx(lo,hi)}
        if front_wall_pos is not None:
            gaps["front"] = _gap_front(lo, hi)
        best_wall, best_gap = min(gaps.items(), key=lambda kv: kv[1])
        if best_gap < SNAP_DIST:
            if best_wall == "back":
                if (hi[normal_axis]-lo[normal_axis]) > SNAP_MAX_DEPTH_FOR_BACK:
                    remaining.append(name); continue
            if best_wall in ("side_min", "side_max"):
                centroid = (lo+hi)/2.0
                cen_gap = abs(centroid[perp_axis] - (side_min_pos if best_wall=="side_min" else side_max_pos))
                if cen_gap > best_gap*SNAP_SIDE_CENTROID_RATIO and cen_gap > SNAP_SIDE_CENTROID_MIN:
                    remaining.append(name); continue
            if best_wall == "front" and front_wall_pos is not None:
                centroid = (lo+hi)/2.0
                cen_gap_front = abs(front_wall_pos - centroid[normal_axis])
                if cen_gap_front > best_gap*SNAP_SIDE_CENTROID_RATIO and cen_gap_front > SNAP_SIDE_CENTROID_MIN:
                    remaining.append(name); continue
            assignments[name] = best_wall
            print(f"  [Assign/snap] {name} -> {best_wall}  gap={best_gap:.4f}")
        else:
            remaining.append(name)

    if not remaining:
        return assignments

    centroids = {n: (world_aabbs[n][0]+world_aabbs[n][1])/2.0 for n in remaining}
    clusters: list = []
    used: set = set()
    for seed in remaining:
        if seed in used: continue
        cluster = [seed]; used.add(seed)
        for other in remaining:
            if other in used: continue
            if np.linalg.norm(centroids[seed]-centroids[other]) < CLUSTER_DIST:
                cluster.append(other); used.add(other)
        clusters.append(cluster)

    for cluster in clusters:
        all_lo = np.array([world_aabbs[n][0] for n in cluster])
        all_hi = np.array([world_aabbs[n][1] for n in cluster])
        clo = all_lo.min(axis=0); chi = all_hi.max(axis=0)

        _cen_n = 0.5*(clo[normal_axis]+chi[normal_axis])
        _cen_p = 0.5*(clo[perp_axis]+chi[perp_axis])
        gaps_cen = {"back": abs(back_wall_pos-_cen_n), "side_min": abs(_cen_p-side_min_pos),
                    "side_max": abs(_cen_p-side_max_pos)}
        if front_wall_pos is not None:
            gaps_cen["front"] = abs(front_wall_pos-_cen_n)
        gaps_face = {"back": _gap_back(clo,chi), "side_min": _gap_side_mn(clo,chi),
                     "side_max": _gap_side_mx(clo,chi)}
        if front_wall_pos is not None:
            gaps_face["front"] = _gap_front(clo, chi)
        gaps_eff = {}
        for w in gaps_cen:
            if w in ("back", "front"):
                gaps_eff[w] = min(float(gaps_cen[w]), float(gaps_face[w]))
            else:
                gaps_eff[w] = float(gaps_cen[w])

        depth_n = float(chi[normal_axis]-clo[normal_axis])
        depth_p = float(chi[perp_axis]-clo[perp_axis])
        SLAB_RATIO = 0.86
        if depth_n+1e-9 < depth_p*SLAB_RATIO:
            pool = {k: gaps_eff[k] for k in ("back","front") if k in gaps_eff}
            wall_label = min(pool, key=pool.get)
        elif depth_p+1e-9 < depth_n*SLAB_RATIO:
            pool = {k: float(gaps_cen[k]) for k in ("side_min","side_max")}
            wall_label = min(pool, key=pool.get)
        else:
            wall_label = min(gaps_eff, key=gaps_eff.get)

        for name in cluster:
            assignments[name] = wall_label
            print(f"  [Assign/centroid] {name} -> {wall_label}")

    return assignments


def compute_world_aabbs(all_names, obj_dir, prefix, poses_dict, base_dy):
    aabbs = {}
    identity_rot = [0.0, 0.0, 0.0, 1.0]
    identity_pos = [0.0, base_dy, 0.0]
    for name in all_names:
        obj_path = os.path.join(obj_dir, f"{prefix}{name}.obj")
        if not os.path.isfile(obj_path):
            continue
        verts = load_obj_verts(obj_path)
        pos, rot = (poses_dict[name]["pos"], poses_dict[name]["rot"]) if name in poses_dict \
                   else (identity_pos, identity_rot)
        vw = transform_verts(verts, pos, rot)
        aabbs[name] = (vw.min(axis=0), vw.max(axis=0))
    return aabbs


def compute_world_geometry(all_names, obj_dir, prefix, poses_dict, base_dy):
    aabbs = {}; clouds = {}
    identity_rot = [0.0, 0.0, 0.0, 1.0]
    identity_pos = [0.0, base_dy, 0.0]
    for name in all_names:
        obj_path = os.path.join(obj_dir, f"{prefix}{name}.obj")
        if not os.path.isfile(obj_path): continue
        verts = load_obj_verts(obj_path)
        pos, rot = (poses_dict[name]["pos"], poses_dict[name]["rot"]) if name in poses_dict \
                   else (identity_pos, identity_rot)
        vw = transform_verts(verts, pos, rot)
        clouds[name] = vw
        aabbs[name]  = (vw.min(axis=0), vw.max(axis=0))
    return aabbs, clouds


def estimate_room_frame_from_floor_wall(floor_wall_names, clouds, scene_min=None, scene_max=None):
    valid = [n for n in floor_wall_names if n in clouds]
    if not valid:
        return None, None, None

    KEYWORDS = ("cabinet", "wardrobe", "bookcase", "closet", "shelf", "counter")
    kw_hits = [n for n in valid if any(k in n.lower() for k in KEYWORDS)]

    def _area(name):
        pts = clouds[name][:, [0,2]]; lo=pts.min(axis=0); hi=pts.max(axis=0)
        return float((hi[0]-lo[0])*(hi[1]-lo[1]))

    def _wall_proximity_score(name):
        pts = clouds[name][:, [0,2]]; c = pts.mean(axis=0); q = pts-c
        cov = (q.T @ q) / max(1, q.shape[0])
        evals, evecs = np.linalg.eigh(cov)
        short_ev = evecs[:, int(np.argmin(evals))]
        d = pts @ short_ev
        obj_lo_d, obj_hi_d = float(d.min()), float(d.max())
        corners_xz = np.array([[scene_min[0],scene_min[2]],[scene_min[0],scene_max[2]],
                                [scene_max[0],scene_min[2]],[scene_max[0],scene_max[2]]], dtype=np.float64)
        d_scene = corners_xz @ short_ev
        s_lo, s_hi = float(d_scene.min()), float(d_scene.max())
        gap = min(abs(obj_lo_d-s_lo), abs(obj_hi_d-s_hi))
        aspect = float(evals[-1]) / max(1e-9, float(evals[0]))
        return aspect / max(1e-6, gap)

    anchor_pool = kw_hits if kw_hits else valid
    if scene_min is not None and scene_max is not None:
        anchor = max(anchor_pool, key=_wall_proximity_score)
    else:
        anchor = max(anchor_pool, key=_area)

    pts = clouds[anchor][:, [0,2]]; c = pts.mean(axis=0); q = pts-c
    cov = (q.T @ q) / max(1, q.shape[0])
    evals, evecs = np.linalg.eigh(cov)
    u = evecs[:, int(np.argmax(evals))]
    u = u / max(1e-12, np.linalg.norm(u))
    if (u[0] < 0) or (abs(u[0]) < 1e-9 and u[1] < 0):
        u = -u
    n = np.array([-u[1], u[0]], dtype=np.float64)
    print(f"  [Frame] anchor={anchor}")
    return anchor, u, n


def estimate_room_frame_from_major_floor_voters(parent_of, clouds, robust_names):
    KEYWORDS = ("sofa","couch","bed","cabinet","wardrobe","bookcase","closet","shelf",
                "desk","table","refrigerator","fridge")
    robust_set  = set(robust_names)
    candidates  = [n for n in robust_set if parent_of.get(n)=="floor" and n in clouds]
    if not candidates:
        return None, None, None

    def _area(name):
        pts = clouds[name][:,[0,2]]; lo=pts.min(axis=0); hi=pts.max(axis=0)
        return float(max(1e-6,(hi[0]-lo[0])*(hi[1]-lo[1])))
    def _boost(name):
        return 2.5 if any(k in name.lower() for k in KEYWORDS) else 1.0

    scores = {n: _area(n)*_boost(n) for n in candidates}
    best   = max(scores.values()) if scores else 0.0
    if best <= 0.0: return None, None, None
    voters = [n for n in candidates if scores[n] >= 0.35*best] or [max(candidates, key=lambda x: scores[x])]

    c2 = 0.0; s2 = 0.0
    for n in voters:
        t = estimate_tangent_from_cloud_xz(clouds[n])
        w = scores[n]; ang = float(np.arctan2(t[1],t[0]))
        c2 += w*np.cos(2.0*ang); s2 += w*np.sin(2.0*ang)

    theta = 0.5 * float(np.arctan2(s2, c2))
    u = np.array([np.cos(theta), np.sin(theta)], dtype=np.float64)
    u = u / max(1e-12, np.linalg.norm(u))
    if (u[0] < 0) or (abs(u[0]) < 1e-9 and u[1] < 0):
        u = -u
    n = np.array([-u[1], u[0]], dtype=np.float64)
    print(f"  [Frame] source=major_floor_voters  voters={voters}")
    return voters, u, n


SEATING_FRAME_KEYWORDS = ("sofa","couch","sectional","loveseat","settee")


def _xz_footprint_area(cloud: np.ndarray) -> float:
    pts = cloud[:,[0,2]]; lo=pts.min(axis=0); hi=pts.max(axis=0)
    return float(max(0.0,(hi[0]-lo[0])*(hi[1]-lo[1])))


def _longest_hull_edge_tangent_xz(vw: np.ndarray):
    pts  = vw[:,[0,2]].astype(np.float64)
    if pts.shape[0] < 2: return None, []
    hull = _convex_hull_xz_points(pts)
    if hull.shape[0] < 2: return None, []
    best_t = None; best_len = 0.0; lens = []
    nh = hull.shape[0]
    for i in range(nh):
        e = hull[(i+1)%nh]-hull[i]; le = float(np.linalg.norm(e)); lens.append(le)
        if le > best_len:
            best_len = le; best_t = e/le
    if best_t is None: return None, lens
    if (best_t[0] < 0) or (abs(best_t[0]) < 1e-9 and best_t[1] < 0):
        best_t = -best_t
    return best_t, sorted(lens, reverse=True)[:5]


def estimate_tangent_longest_hull_edge_xz(vw: np.ndarray) -> np.ndarray:
    t, _ = _longest_hull_edge_tangent_xz(vw)
    return t if t is not None else estimate_tangent_from_cloud_xz(vw)


def estimate_tangent_hull_perp_to_opening_xz(vw: np.ndarray, scene_center_xz: np.ndarray,
                                              min_len_frac: float = 0.28) -> np.ndarray:
    pts  = vw[:,[0,2]].astype(np.float64); c = pts.mean(axis=0)
    to_room = np.asarray(scene_center_xz, dtype=np.float64).reshape(2) - c
    nl = float(np.linalg.norm(to_room))
    if nl < 1e-5: return estimate_tangent_longest_hull_edge_xz(vw)
    n_open = to_room/nl
    hull = _convex_hull_xz_points(pts)
    if hull.shape[0] < 2: return estimate_tangent_from_cloud_xz(vw)
    nh = hull.shape[0]
    elens = [float(np.linalg.norm(hull[(i+1)%nh]-hull[i])) for i in range(nh)]
    max_len = max(elens) if elens else 0.0
    min_len = min_len_frac * max_len if max_len > 1e-9 else 0.0
    best_score = -1.0; best_le = 0.0; best_t = None
    for i in range(nh):
        le = elens[i]
        if le < min_len: continue
        e = hull[(i+1)%nh]-hull[i]; raw_t = e/max(1e-12,le)
        ad = float(abs(np.dot(raw_t, n_open)))
        score = le * float(np.sqrt(max(0.0, 1.0-ad*ad)))
        if score > best_score+1e-12:
            best_score=score; best_le=le; best_t=raw_t.copy()
        elif abs(score-best_score) <= 1e-12 and le > best_le:
            best_le=le; best_t=raw_t.copy()
    if best_t is None: return estimate_tangent_longest_hull_edge_xz(vw)
    if (best_t[0] < 0) or (abs(best_t[0]) < 1e-9 and best_t[1] < 0):
        best_t = -best_t
    return best_t


def maybe_align_frame_to_primary_seating(parent_of, clouds, robust_names, u_xz, n_xz,
                                          anchor_label, anchor_name_for_area=None,
                                          min_seat_area_m2=0.55, min_angle_deg=6.0,
                                          seating_area_vs_anchor_ratio=1.12, scene_center_xz=None):
    candidates = [n for n in robust_names if n in clouds and parent_of.get(n)=="floor"
                  and any(k in n.lower() for k in SEATING_FRAME_KEYWORDS)]
    if not candidates: return u_xz, n_xz, None
    seat = max(candidates, key=lambda n: _xz_footprint_area(clouds[n]))
    area = _xz_footprint_area(clouds[seat])
    if area < min_seat_area_m2:
        return u_xz, n_xz, None

    _vc = clouds[seat]
    u_s = (estimate_tangent_hull_perp_to_opening_xz(_vc, scene_center_xz)
           if scene_center_xz is not None else None)
    u_s = u_s if u_s is not None else estimate_tangent_longest_hull_edge_xz(_vc)
    u_s = u_s / max(1e-12, np.linalg.norm(u_s))
    if (u_s[0] < 0) or (abs(u_s[0]) < 1e-9 and u_s[1] < 0): u_s = -u_s
    n_s = np.array([-u_s[1], u_s[0]], dtype=np.float64)

    cos_al  = float(np.clip(np.dot(u_xz, u_s), -1.0, 1.0))
    ang_deg = float(np.degrees(np.arccos(cos_al)))

    if ang_deg < min_angle_deg:
        cab_a = _xz_footprint_area(clouds[anchor_name_for_area]) \
                if anchor_name_for_area and anchor_name_for_area in clouds else 0.0
        if cab_a > 1e-6 and area >= seating_area_vs_anchor_ratio*cab_a and area >= min_seat_area_m2:
            return u_s, n_s, seat
        return u_xz, n_xz, None

    print(f"  [Frame/seating] OVERRIDE from '{seat}' (area={area:.3f}): angle={ang_deg:.1f}deg")
    return u_s, n_s, seat


def build_local_aabbs_from_frame(clouds, u_xz, n_xz):
    local = {}
    for name, vw in clouds.items():
        xz = vw[:,[0,2]]; u=xz@u_xz; v=xz@n_xz; y=vw[:,1]
        lo = np.array([u.min(), y.min(), v.min()], dtype=np.float64)
        hi = np.array([u.max(), y.max(), v.max()], dtype=np.float64)
        local[name] = (lo, hi)
    return local


def estimate_tangent_from_cloud_xz(vw: np.ndarray):
    pts = vw[:,[0,2]]; c=pts.mean(axis=0); q=pts-c
    cov = (q.T @ q) / max(1, q.shape[0])
    evals, evecs = np.linalg.eigh(cov)
    t = evecs[:, int(np.argmax(evals))]
    return t / max(1e-12, np.linalg.norm(t))


def estimate_normal_from_cloud_xz(vw: np.ndarray):
    pts = vw[:,[0,2]]; c=pts.mean(axis=0); q=pts-c
    cov = (q.T @ q) / max(1, q.shape[0])
    evals, evecs = np.linalg.eigh(cov)
    n = evecs[:, int(np.argmin(evals))]
    return n / max(1e-12, np.linalg.norm(n))


