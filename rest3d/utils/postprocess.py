def parse_scene_tree(tree_path: str):
    tree = json.load(open(tree_path))
    parent_of = {}
    type_of   = {}
    for e in tree["edges"]:
        parent_of[e["child"]] = e["parent"]
        type_of[e["child"]]   = e["type"]
    return parent_of, type_of


def detect_file_prefix(obj_dir: str, sample_name: str) -> str:
    for fname in os.listdir(obj_dir):
        if fname.endswith(f"{sample_name}.obj"):
            return fname[: -len(f"{sample_name}.obj")]
    return ""


